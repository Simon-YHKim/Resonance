export function pickEnemyResponse(
  action: CombatAction,
  hpRatio: number,
  seed: number,
): string {
  const v = ENEMY_RESPONSES[action];
  const tier = hpRatio > 0.66 ? v.high : hpRatio > 0.33 ? v.mid : v.low;
  return tier[seed % tier.length];
}
